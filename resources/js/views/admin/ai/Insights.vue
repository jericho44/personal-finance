                        <i v-if="!aiStore.loading" class="fa fa-sync me-2"></i>
                        <span v-else class="spinner-border spinner-border-sm me-2"></span>
                        Refresh Insights
                    </button>
                </div>
            </div>

            <!-- Body -->
            <div class="card-body pt-0">
                <div v-if="aiStore.loading && !aiStore.insight" class="d-flex flex-column align-items-center py-20">
                    <div class="spinner-border text-primary w-40px h-40px mb-5" role="status"></div>
                    <span class="text-muted fw-bold">Gemini is analyzing your finances...</span>
                </div>

                <div v-else-if="aiStore.error" class="alert alert-danger d-flex align-items-center p-5 mb-10">
                    <i class="fa fa-exclamation-triangle fs-2hx text-danger me-4"></i>
                    <div class="d-flex flex-column">
                        <h4 class="mb-1 text-danger">Insight Error</h4>
                        <span>{{ aiStore.error }}</span>
                    </div>
                </div>

                <div v-else-if="aiStore.insight" class="insight-content py-5">
