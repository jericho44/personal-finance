                    <button @click="aiStore.fetchInsights()" class="btn btn-primary mt-5">Generate First Insight</button>
                </div>
            </div>
        </div>
    </div>
</template>

<script setup lang="ts">
import { onMounted, computed } from 'vue';
import { useAIInsightStore } from '@/stores/aiInsight';
import { marked } from 'marked';
import { initializeAppPlugins } from '@/plugins/global';

const aiStore = useAIInsightStore();

const renderedMarkdown = computed(() => {
    return aiStore.insight ? marked(aiStore.insight) : '';
});

onMounted(() => {
    initializeAppPlugins();
    if (!aiStore.insight) {
        aiStore.fetchInsights();
    }
});
</script>

